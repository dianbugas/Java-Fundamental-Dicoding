package com.javafundamental.perulangan;

public class For {
    public static void main(String[] args){
        for (int i = 10; i >= 0; i--) {
            System.out.println("Angka : " + i);
        }
        for (int a = 0; a <= 10; a++){
            System.out.println("angka : " + a);
        }
    }
}
